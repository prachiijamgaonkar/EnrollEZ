package com.example.admissionexamform.model;

public class modelAdmissionEligibility {

    String USNNo, StudentName, CollegeId, year;

    public modelAdmissionEligibility() {
    }

    public String getUSNNo() {
        return USNNo;
    }

    public void setUSNNo(String USNNo) {
        this.USNNo = USNNo;
    }

    public String getStudentName() {
        return StudentName;
    }

    public void setStudentName(String studentName) {
        StudentName = studentName;
    }

    public String getCollegeId() {
        return CollegeId;
    }

    public void setCollegeId(String collegeId) {
        CollegeId = collegeId;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
